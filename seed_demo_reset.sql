-- ============================================================
-- SECTION 4: admin_config — emergency_count (대시보드용)
-- ============================================================

MERGE INTO admin_config t
USING (SELECT 'emergency_count' AS config_key, '3' AS config_value, UNISTR('\C751\AE09 \BC31\C0C1 \C6B4\C601\C790 \C218\B3D9 \C124\C815\AC12 (0~10)') AS description FROM dual) s
ON (t.config_key = s.config_key)
WHEN MATCHED THEN
    UPDATE SET t.config_value = s.config_value, t.updated_at = SYSTIMESTAMP
WHEN NOT MATCHED THEN
    INSERT (config_key, config_value, description, updated_at)
    VALUES (s.config_key, s.config_value, s.description, SYSTIMESTAMP);

COMMIT;

