-- SECTION 2: IDENTITY 시퀀스 리셋 — 번호 체계로 도메인 구분
-- [v3.2] 예약 21xxx / 접수 11xxx 분리:
--   - 번호만 보고 "예약" vs "접수" 즉시 식별 가능
--   - 예약자가 예약내원 접수하면 admin_visit IDENTITY가 자연스럽게 11xxx 발급
--   - 실제 의료정보시스템(OCS/EMR)의 일반적 관례와 일치
-- ============================================================

ALTER TABLE admin_reservation MODIFY reservation_id GENERATED BY DEFAULT ON NULL AS IDENTITY (START WITH 21001);
ALTER TABLE admin_visit       MODIFY visit_id       GENERATED BY DEFAULT ON NULL AS IDENTITY (START WITH 11001);
ALTER TABLE admin_appointment MODIFY appointment_id GENERATED BY DEFAULT ON NULL AS IDENTITY (START WITH 21001);
ALTER TABLE admin_invoice     MODIFY invoice_id     GENERATED BY DEFAULT ON NULL AS IDENTITY (START WITH 1);
ALTER TABLE admin_queue_ticket MODIFY ticket_id     GENERATED BY DEFAULT ON NULL AS IDENTITY (START WITH 1);

